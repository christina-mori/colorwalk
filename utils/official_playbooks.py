def build_official_playbook_records(base_dir: str | Path, timestamp: str) -> list[dict]:
    gallery_dir = Path(base_dir) / "static" / "gallery"
    records: list[dict] = []
    for index, item in enumerate(OFFICIAL_PLAYBOOK_ITEMS, start=1):
        image_file = gallery_dir / item["image"]
        width, height = 1, 1
        if image_file.exists():
            with Image.open(image_file) as img:
                width, height = img.size
        records.append(
            {
                "id": f"official:{item['id']}",
                "source_type": "official",
                "source_ref_id": item["id"],
                "mode": item["mode"],
                "display_name": item.get("display_name", ""),
                "description": item.get("description", ""),
                "settings_json": json.dumps(item["settings"], ensure_ascii=False, separators=(",", ":")),
                "image_path": f"gallery/{item['image']}",
                "image_width": width,
                "image_height": height,
                "is_visible": 1,
                "sort_rank": index * 100,
                "created_at": timestamp,
                "updated_at": timestamp,
                "published_at": timestamp,
            }
        )
    return records
